print("AULA 15 - ESTRUTURAS DE REPETIÇÃO - comando while")
"""
Laços de repetição/Estruturas de repetição/Iteradores
* Repetem um trecho do código:
	- enquanto uma condição avaliada for verdadeira;
	- durante uma pré-determinada condição
* Comando while (enquanto)
"""
x = 1

while x < 10:
	print(x)
	x += 1 # x = x + 1
