print("AULA 17 - ESTRUTURAS DE REPETIÇÃO - comandos for e range")
# range - retorna uma lista
print("EXEMPLO 1")
for i in range (10): #LEMBRANDO QUE A CONTAGEM SE DÁ A PARTIR DO NÚMERO ZERO
	print(i)
print("EXEMPLO 2")
for i in range (10,20): # IMPRIME DE 10 ATÉ 20
	print(i)
print("EXEMPLO 3")
for i in range (10,20,2): # IMPRIME DE 10 ATÉ 20 DE DOIS EM DOIS NÚMEROS
	print(i)
