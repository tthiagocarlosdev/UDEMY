print(" ")
lista1 = [21, 3, 7, 8, 9, 15, 6]
lista1.sort() # "sort" ordena a lista alterando a original
print(lista1)

print(" ")
lista1.reverse() # reverete a lista
print(lista1)

print(" ")
lista1 = sorted(lista1) # retorna uma lista ordenada
print(lista1)

print(" ")
lista1.sort(reverse=True) # ordena de forma decrescente
print(lista1)

print(" ")
lista1.reverse()
print(lista1)

