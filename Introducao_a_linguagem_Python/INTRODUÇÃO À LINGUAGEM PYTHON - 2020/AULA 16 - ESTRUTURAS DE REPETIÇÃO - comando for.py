print("AULA 16 - ESTRUTURAS DE REPETIÇÃO - comando for")
# for (para) percorre um conjunto de dados pré-estabelecidos
# listas - sequencia de números ou caractéres
lista1 = [1,2,3,4,5]
lista2 = ["ola", "mundo","!"]
lista3 = [0,"ola","biscoito","bolacha",9.99,True]

print("LISTA 1")
for i in lista1:
	print(i)
print("LISTA 2")
for i in lista2:
	print(i)
print("LISTA 3")
for i in lista3:
	print(i)
