# -*- coding: utf-8 -*-
print("AULA 9 - VARIÁVEIS E TIPOS DE DADOS")
# Variável - permite o armazenamento de informação na memória.
print("Olá mundo!")
print("USADO VARIÁVEL")
minha_variavel = "Olá mundo!"
print(minha_variavel)
print(minha_variavel)
print(minha_variavel)
"""
Regras básicas de variável:
Sintaxe: nome_da_variavel = valor
- Não deve ter caracteres especiais e nem espaços (coloca _ entre os espaços)
- É case sensitive (letra maiúscula é diferente de letra minúscula)
- [ = ] Operador de atribuição - Operador que indica que determinado valor vai ser aplicado a variável.
- Valor - define o tipo de variável
Tipos de Variável:
* Numérico inteiro - Número inteiro
  45
* Numérico flutuante - Número decimal
  12.89
* Textual(string) - recebe caracteres
  "Olá mundo"
* Booleana - recebe valores
  True (verdadeiro) ou False (falso)
"""
print("Exemplos de variáveis")
var1 = 1 # variável inteira
var2 = 1.1 # variável float
var3 = "eu sou uma string" # variável string
var4 = True # veradeiro
var4 = False # falso
print(var1)
print(var2)
print(var3)
print(var4)
